            <p>About Us

Cedars of Lebanon, is an organisation that exists to raise a banner for Jesus Christ to all nations by raising a standard of Christian living, honest, bearing fruit of the Holy Spirit wherever the members are established. Its members are the Born again Alumni of the Christian of Mbarara University of Science and technology and their Born Again spouses. The associate membership of this organisation is open to Born-Again children of full members aged 18 years and above and any other Born-Again Christian interested in the vision of the organisation. The Associate members are required to express their interest in writing and be seconded by two (2) Cedars.

The organisation has its head Quarters in Kampala Uganda but it has activities in five continents (excluding South America where opportunities have not opened). The members of the organisation are called ‘Cedars’. The main activities are in Uganda, East Africa.

The vision of the company is:To raise the banner of Jesus Christ for the nations.” Isaiah 62:10

The company’s mission is:To flourish in righteousness, always bear fruit, stay fresh and proclaim the Gospel” Psalm 92:12-15 (NIV)

The organisation has the following objectives among others:<br/>

To encourage the Cedars to flourish in righteousness.<br/>

To encourage Cedars to always bear fruit.<br/>

To proclaim the gospel of Jesus Christ.<br/>

To promote the qualitative and quantitative growth of Mbarara University of Science & Technology – Christian Union<br/>

To collaborate with other Christian organizations in furthering its vision.<br/>

To support social functions as shall be beneficial to the promotion of the Company’s Vision.<br/>

